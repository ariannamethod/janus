static void build_extended_vocab(void) {
    if (ext_vocab) free(ext_vocab);
    ext_vocab = (ExtVocabEntry *)malloc(MAX_EXT_VOCAB * sizeof(ExtVocabEntry));
    ext_vocab_n = 0;

    /* First: all hardcoded words */
    for (int i = 0; i < NWORDS && ext_vocab_n < MAX_EXT_VOCAB; i++) {
        strncpy(ext_vocab[ext_vocab_n].word, WORDS[i], MAX_WORD_LEN - 1);
        ext_vocab[ext_vocab_n].word[MAX_WORD_LEN - 1] = 0;
        memcpy(ext_vocab[ext_vocab_n].bpe_ids, vocab_bpe[i], vocab_bpe_len[i] * sizeof(int));
        ext_vocab[ext_vocab_n].bpe_len = vocab_bpe_len[i];
        ext_vocab[ext_vocab_n].from_hardcoded = 1;
        ext_vocab_n++;
    }

    /* Second: scan BPE tokens for whole words */
    int bpe_added = 0;
    for (int tid = 0; tid < BPE_VOCAB && ext_vocab_n < MAX_EXT_VOCAB; tid++) {
        char decoded[64];
        int dlen = bpe_decode_token(tid, decoded, 64);
        if (dlen == 0) continue;

        /* Strip leading/trailing spaces */
        char *s = decoded;
        while (*s == ' ') s++;
        int sl = (int)strlen(s);
        while (sl > 0 && s[sl - 1] == ' ') s[--sl] = 0;

        if (sl < 3) continue;

        /* Check all lowercase alpha */
        int ok = 1;
        for (int j = 0; j < sl; j++) {
            if (!islower((unsigned char)s[j])) { ok = 0; break; }
        }
        if (!ok) continue;

        /* Check not already in ext_vocab */
        int dup = 0;
        for (int j = 0; j < ext_vocab_n; j++) {
            if (strcmp(ext_vocab[j].word, s) == 0) { dup = 1; break; }
        }
        if (dup) continue;

        if (is_stop_word(s) || is_suffix_fragment(s)) continue;

        strncpy(ext_vocab[ext_vocab_n].word, s, MAX_WORD_LEN - 1);
        ext_vocab[ext_vocab_n].word[MAX_WORD_LEN - 1] = 0;
        ext_vocab[ext_vocab_n].bpe_len = bpe_encode(s, ext_vocab[ext_vocab_n].bpe_ids, MAX_BPE_PER_WORD);
        ext_vocab[ext_vocab_n].from_hardcoded = 0;
        ext_vocab_n++;
        bpe_added++;
    }

    printf("extended vocab: %d words (%d hardcoded + %d from BPE)\n",
           ext_vocab_n, NWORDS, bpe_added);
}

static void load_vocabulary(const char *path) {
    FILE *f = fopen(path, "r");
    if (!f) {
        fprintf(stderr, "Error: cannot open vocabulary file: %s\n", path);
        exit(1);
    }
    char line[256];
    NWORDS = 0;
    while (fgets(line, sizeof(line), f) && NWORDS < MAX_WORDS) {
        /* Strip whitespace */
        int len = (int)strlen(line);
        while (len > 0 && (line[len - 1] == '\n' || line[len - 1] == '\r' || line[len - 1] == ' '))
            line[--len] = 0;
        if (len == 0) continue;
        strncpy(WORDS[NWORDS], line, MAX_WORD_LEN - 1);
        WORDS[NWORDS][MAX_WORD_LEN - 1] = 0;
        NWORDS++;
    }
    fclose(f);

    /* Precompute BPE encoding for each word */
    for (int i = 0; i < NWORDS; i++) {
        vocab_bpe_len[i] = bpe_encode(WORDS[i], vocab_bpe[i], MAX_BPE_PER_WORD);
    }

    build_extended_vocab();
}

static int try_stem(const char *word) {
    int wlen = (int)strlen(word);
    for (int s = 0; s < N_SUFFIXES; s++) {
        int slen = (int)strlen(SUFFIXES[s]);
        if (wlen > slen + 2) {
            if (strcmp(word + wlen - slen, SUFFIXES[s]) == 0) {
                char stem[MAX_WORD_LEN];
                int stem_len = wlen - slen;
                memcpy(stem, word, stem_len);
                stem[stem_len] = 0;
                int idx = word_index(stem);
                if (idx >= 0) return idx;
            }
        }
    }
    return -1;
}

static int tokenize_words(const char *text, int *ids, int max_ids) {
    int n = 0;
    char buf[1024];
    int bi = 0;
    int tlen = (int)strlen(text);

    for (int i = 0; i <= tlen; i++) {
        char c = (i < tlen) ? text[i] : 0;
        if (isalpha((unsigned char)c)) {
            if (bi < (int)sizeof(buf) - 1)
                buf[bi++] = tolower((unsigned char)c);
        } else {
            if (bi > 0) {
                buf[bi] = 0;
                bi = 0;
                if (n >= max_ids) break;
                int idx = word_index(buf);
                if (idx >= 0) {
                    ids[n++] = idx;
                    continue;
                }
                int stem_idx = try_stem(buf);
                if (stem_idx >= 0) {
                    ids[n++] = stem_idx;
                }
            }
        }
    }
    return n;
}

/* ===================================================================
 * CALENDAR DRIFT (Metonic cycle)
 * =================================================================== */
#define AM_ANNUAL_DRIFT    11.25f
#define AM_GREGORIAN_YEAR  365.25f
#define AM_METONIC_YEARS   19
#define AM_METONIC_LEAPS   7
#define AM_MAX_UNCORRECTED 33.0f
#define EPOCH_TIMESTAMP    1727956800.0

static const int METONIC_LEAP_YEARS[7] = {3, 6, 8, 11, 14, 17, 19};

static int calendar_days_since_epoch(void) {
    double now = (double)time(NULL);
    return (int)((now - EPOCH_TIMESTAMP) / 86400.0);
}

static float calendar_cumulative_drift(int days) {
    float years = days / AM_GREGORIAN_YEAR;
    float base_drift = years * AM_ANNUAL_DRIFT;
    int full_cycles = (int)(years / AM_METONIC_YEARS);
    float corrections = (float)(full_cycles * AM_METONIC_LEAPS * 30);
    float partial = fmodf(years, (float)AM_METONIC_YEARS);
    int yic = (int)partial + 1;
    for (int i = 0; i < AM_METONIC_LEAPS; i++) {
        if (METONIC_LEAP_YEARS[i] <= yic)
            corrections += 30.0f;
    }
    return base_drift - corrections;
}

static float calendar_dissonance(int days) {
    float drift = calendar_cumulative_drift(days);
    float raw = fabsf(fmodf(drift, AM_MAX_UNCORRECTED)) / AM_MAX_UNCORRECTED;
    if (raw < 0.0f) raw = 0.0f;
    if (raw > 1.0f) raw = 1.0f;
    return raw;
}

