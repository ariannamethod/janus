/* ===================================================================
 * SIMPLE RNG (xorshift32)
 * =================================================================== */
static unsigned int rng_state = 12345;

static void rng_seed(unsigned int s) { rng_state = s ? s : 1; }
static unsigned int rng_next(void) {
    rng_state ^= rng_state << 13;
    rng_state ^= rng_state >> 17;
    rng_state ^= rng_state << 5;
    return rng_state;
}
static float rng_float(void) { return (rng_next() & 0x7FFFFFFF) / (float)0x7FFFFFFF; }
static int rng_int(int n) { return (int)(rng_float() * n) % n; }

/* ===================================================================
 * BPE TOKENIZER
 * =================================================================== */
static int bpe_encode(const char *text, int *out, int max_out) {
    int slen = (int)strlen(text);
    int *seq = (int *)malloc(slen * sizeof(int));
    int n = 0;
    for (int i = 0; i < slen; i++) {
        unsigned char c = (unsigned char)text[i];
        if (c >= 128) continue; /* skip non-ASCII */
        seq[n++] = tolower(c);
    }
    for (int m = 0; m < BPE_MERGES; m++) {
        int left = BPE_TABLE[m][0];
        int right = BPE_TABLE[m][1];
        int new_id = 256 + m;
        int j = 0;
        for (int i = 0; i < n; i++) {
            if (i < n - 1 && seq[i] == left && seq[i + 1] == right) {
                seq[j++] = new_id;
                i++;
            } else {
                seq[j++] = seq[i];
            }
        }
        n = j;
    }
    int out_n = n < max_out ? n : max_out;
    for (int i = 0; i < out_n; i++) out[i] = seq[i];
    free(seq);
    return out_n;
}

/* Decode a single BPE token ID to string. Returns length written, or 0 on failure. */
static int bpe_decode_token(int token_id, char *buf, int bufsize) {
    if (token_id < 0 || token_id >= BPE_VOCAB) return 0;
    if (token_id < 256) {
        if (token_id >= 32 && token_id < 127) {
            if (bufsize < 2) return 0;
            buf[0] = (char)token_id;
            buf[1] = 0;
            return 1;
        }
        return 0;
    }
    int merge_idx = token_id - 256;
    if (merge_idx >= BPE_MERGES) return 0;
    int left = BPE_TABLE[merge_idx][0];
    int right = BPE_TABLE[merge_idx][1];
    char lbuf[64], rbuf[64];
    int llen = bpe_decode_token(left, lbuf, 64);
    int rlen = bpe_decode_token(right, rbuf, 64);
    if (llen == 0 || rlen == 0) return 0;
    if (llen + rlen >= bufsize) return 0;
    memcpy(buf, lbuf, llen);
    memcpy(buf + llen, rbuf, rlen);
    buf[llen + rlen] = 0;
    return llen + rlen;
}

