/* matmul: W[rows, cols] @ x[cols] -> out[rows] */
static void matmul_mv(const float *W, const float *x, float *out, int rows, int cols) {
    for (int i = 0; i < rows; i++) {
        float s = 0.0f;
        const float *row = W + i * cols;
        for (int j = 0; j < cols; j++) s += row[j] * x[j];
        out[i] = s;
    }
}

/* RMSNorm: out[i] = g[i] * x[i] / rms(x) */
static void rmsnorm(const float *x, const float *g, float *out, int n) {
    float ss = 0.0f;
    for (int i = 0; i < n; i++) ss += x[i] * x[i];
    ss = ss / n + 1e-5f;
    float inv = 1.0f / sqrtf(ss);
    for (int i = 0; i < n; i++) out[i] = g[i] * x[i] * inv;
}

/* Apply RoPE to q and k. Layout: [seq_len * N_HEADS * HEAD_DIM] as [t][h][d] */
static void apply_rope(float *q, float *k, int seq_len) {
    float theta_base = 10000.0f;
    for (int t = 0; t < seq_len; t++) {
        for (int h = 0; h < N_HEADS; h++) {
            int offset = (t * N_HEADS + h) * HEAD_DIM;
            for (int d = 0; d < HEAD_DIM / 2; d++) {
                float freq = 1.0f / powf(theta_base, 2.0f * d / HEAD_DIM);
                float cos_f = cosf(t * freq);
                float sin_f = sinf(t * freq);
                /* rotate q */
                float q0 = q[offset + d];
                float q1 = q[offset + d + HEAD_DIM / 2];
                q[offset + d]              = q0 * cos_f - q1 * sin_f;
                q[offset + d + HEAD_DIM/2] = q0 * sin_f + q1 * cos_f;
                /* rotate k */
                float k0 = k[offset + d];
                float k1 = k[offset + d + HEAD_DIM / 2];
                k[offset + d]              = k0 * cos_f - k1 * sin_f;
                k[offset + d + HEAD_DIM/2] = k0 * sin_f + k1 * cos_f;
            }
        }
    }
}

/*
 * Full forward pass through 8-layer Resonance engine.
 * Input:  bpe_ids[S]
 * Output: bpe_logits[BPE_VOCAB] (logits for last position)
 */
static void forward_pass(const int *bpe_ids, int S, float *bpe_logits) {
    if (S < 1) S = 1;
    if (S > MAX_SEQ) S = MAX_SEQ;

    /* x: [S * DIM] residual stream — tok_emb + pos_emb */
    float *x = alloc_floats(S * DIM);
    for (int t = 0; t < S; t++) {
        int tok = bpe_ids[t];
        if (tok < 0 || tok >= BPE_VOCAB) tok = 0;
        for (int d = 0; d < DIM; d++)
            x[t * DIM + d] = model.tok_emb[tok * DIM + d] + model.pos_emb[t * DIM + d];
    }

    /* Temporary buffers */
    float *h       = alloc_floats(S * DIM);
    float *q       = alloc_floats(S * DIM);
    float *k       = alloc_floats(S * DIM);
    float *v       = alloc_floats(S * DIM);
    float *av      = alloc_floats(S * DIM);
    float *qkv_out = alloc_floats(S * DIM);
    float *rrp     = alloc_floats(S * DIM);
    float *h2      = alloc_floats(S * DIM);
    float hn[DIM], qt[DIM], kt[DIM], vt[DIM], wo_out[DIM];
    float fg[HDIM], fu[HDIM], sw[HDIM], fd[DIM];
    float rrp_t[DIM], hn2[DIM];

    for (int l = 0; l < N_LAYERS; l++) {
        LayerWeights *lw = &model.layers[l];

        /* 1. h = rmsnorm(x, attn_norm) for each position */
        for (int t = 0; t < S; t++) {
            rmsnorm(x + t * DIM, lw->attn_norm, hn, DIM);
            memcpy(h + t * DIM, hn, DIM * sizeof(float));
        }

        /* 2. q = h @ wq, k = h @ wk, v = h @ wv */
        for (int t = 0; t < S; t++) {
            matmul_mv(lw->wq, h + t * DIM, qt, DIM, DIM);
            matmul_mv(lw->wk, h + t * DIM, kt, DIM, DIM);
            matmul_mv(lw->wv, h + t * DIM, vt, DIM, DIM);
            memcpy(q + t * DIM, qt, DIM * sizeof(float));
            memcpy(k + t * DIM, kt, DIM * sizeof(float));
            memcpy(v + t * DIM, vt, DIM * sizeof(float));
        }

        /* 3. Apply RoPE */
        apply_rope(q, k, S);

        /* 4. Multi-head causal attention */
        float scale = 1.0f / sqrtf((float)HEAD_DIM);
        memset(av, 0, S * DIM * sizeof(float));

        for (int hd = 0; hd < N_HEADS; hd++) {
            for (int ti = 0; ti < S; ti++) {
                int qi_off = (ti * N_HEADS + hd) * HEAD_DIM;
                /* Compute attention scores for causal window */
                float scores[MAX_SEQ];
                float maxs = -1e30f;
                for (int tj = 0; tj <= ti; tj++) {
                    int kj_off = (tj * N_HEADS + hd) * HEAD_DIM;
                    float dot = 0.0f;
                    for (int d = 0; d < HEAD_DIM; d++)
                        dot += q[qi_off + d] * k[kj_off + d];
                    dot *= scale;
                    scores[tj] = dot;
                    if (dot > maxs) maxs = dot;
                }
                /* Softmax */
                float esum = 0.0f;
                for (int tj = 0; tj <= ti; tj++) {
                    scores[tj] = expf(scores[tj] - maxs);
                    esum += scores[tj];
                }
                if (esum > 0.0f) {
                    float inv_s = 1.0f / esum;
                    for (int tj = 0; tj <= ti; tj++) scores[tj] *= inv_s;
                }
                /* Weighted value sum */
                int av_off = (ti * N_HEADS + hd) * HEAD_DIM;
                for (int tj = 0; tj <= ti; tj++) {
                    float a = scores[tj];
                    if (a == 0.0f) continue;
                    int vj_off = (tj * N_HEADS + hd) * HEAD_DIM;
                    for (int d = 0; d < HEAD_DIM; d++)
                        av[av_off + d] += a * v[vj_off + d];
                }
            }
        }

        /* Project through wo */
        for (int t = 0; t < S; t++) {
            matmul_mv(lw->wo, av + t * DIM, wo_out, DIM, DIM);
            memcpy(qkv_out + t * DIM, wo_out, DIM * sizeof(float));
        }

        /* 5. RRPRAM resonance: rrp = h @ wr */
        for (int t = 0; t < S; t++) {
            matmul_mv(lw->wr, h + t * DIM, rrp_t, DIM, DIM);
            memcpy(rrp + t * DIM, rrp_t, DIM * sizeof(float));
        }

        /* 6. Gate weights: softmax(gate[0], gate[1]) */
        float g0 = lw->gate[0], g1 = lw->gate[1];
        float gmax = g0 > g1 ? g0 : g1;
        float e0 = expf(g0 - gmax), e1 = expf(g1 - gmax);
        float gsum = e0 + e1;
        float w0 = e0 / gsum, w1 = e1 / gsum;

        /* 7. x = x + w0*qkv_out + w1*rrp (gated residual) */
        for (int i = 0; i < S * DIM; i++)
            x[i] += w0 * qkv_out[i] + w1 * rrp[i];

        /* 8. h2 = rmsnorm(x, ffn_norm) */
        for (int t = 0; t < S; t++) {
            rmsnorm(x + t * DIM, lw->ffn_norm, hn2, DIM);
            memcpy(h2 + t * DIM, hn2, DIM * sizeof(float));
        }

        /* 9. SwiGLU FFN: x = x + w_down @ (silu(h2 @ w_gate) * (h2 @ w_up)) */
        for (int t = 0; t < S; t++) {
            matmul_mv(lw->w_gate, h2 + t * DIM, fg, HDIM, DIM);
            matmul_mv(lw->w_up,   h2 + t * DIM, fu, HDIM, DIM);
            for (int i = 0; i < HDIM; i++) sw[i] = silu(fg[i]) * fu[i];
            matmul_mv(lw->w_down, sw, fd, DIM, HDIM);
            for (int d = 0; d < DIM; d++) x[t * DIM + d] += fd[d];
        }
    }

    /* After all layers: final rmsnorm + lm_head for LAST position */
    float xn[DIM];
    rmsnorm(x + (S - 1) * DIM, model.final_norm, xn, DIM);
    matmul_mv(model.lm_head, xn, bpe_logits, BPE_VOCAB, DIM);

    free(x); free(h); free(q); free(k); free(v);
    free(av); free(qkv_out); free(rrp); free(h2);
}

/* ===================================================================
 * BPE logits to word scores
 * =================================================================== */
static void bpe_logits_to_word_scores(const float *bpe_logits, float *scores, int use_ext) {
    if (use_ext) {
        for (int w = 0; w < ext_vocab_n; w++) {
            int bl = ext_vocab[w].bpe_len;
            if (bl == 0) { scores[w] = 0.0f; continue; }
            float s = 0.0f;
            for (int j = 0; j < bl; j++) {
                int tok = ext_vocab[w].bpe_ids[j];
                if (tok >= 0 && tok < BPE_VOCAB) s += bpe_logits[tok];
            }
            scores[w] = s / bl;
        }
    } else {
        for (int w = 0; w < NWORDS; w++) {
            int bl = vocab_bpe_len[w];
            if (bl == 0) { scores[w] = 0.0f; continue; }
            float s = 0.0f;
            for (int j = 0; j < bl; j++) {
                int tok = vocab_bpe[w][j];
                if (tok >= 0 && tok < BPE_VOCAB) s += bpe_logits[tok];
            }
            scores[w] = s / bl;
        }
    }
}

/* ===================================================================
 * DARIO OVERLAY
 * =================================================================== */
static void dario_overlay(float *logits, const int *word_context, int n_wctx,
                           int prev_word, int step_idx, int direction, int use_ext)
{
    float alpha_mod = 1.0f + 0.3f * chambers[CH_LOVE] - 0.2f * chambers[CH_RAGE] + 0.1f * chambers[CH_FLOW];
    float gamma_mod = 1.0f + 0.4f * chambers[CH_VOID] + 0.2f * chambers[CH_COMPLEX];
    float cal_mod = 1.0f + 0.2f * calendar_dissonance(calendar_days_since_epoch());
    float dir_mod = (direction == -1) ? 0.8f : 1.2f;
    float gate_r = 1.0f / (1.0f + expf(-(resonance_field - 0.5f) * 4.0f));
    float h_g = silu(gate_r * 2.0f);
    float f_g = silu(gate_r * 1.5f);

    int n_logits = use_ext ? ext_vocab_n : NWORDS;
    for (int v = 0; v < n_logits; v++) {
        int is_hardcoded = (v < NWORDS);

        /* B: bigram transition */
        float B = 0.0f;
        if (prev_word >= 0 && is_hardcoded)
            B = logf(1.0f + bigram_get(prev_word, v)) * 4.0f;

        /* H: Hebbian co-occurrence */
        float H = 0.0f;
        if (is_hardcoded) {
            for (int ci = 0; ci < n_wctx; ci++)
                H += logf(1.0f + cooc_get(word_context[ci], v));
            H /= (n_wctx + 1);
        }

        /* F: Prophecy fulfillment */
        float F = prophecy_debt * (1.0f + rng_float() * 0.5f);

        /* A: Destiny attraction */
        float A = destiny_bias * gamma_mod * 0.5f;

        logits[v] += (B + alpha_mod * 3.0f * H * h_g + 2.0f * F * f_g + A) * dir_mod * cal_mod;
    }
}

/* ===================================================================
 * WORD SELECTION
 * =================================================================== */
typedef struct {
    char word[MAX_WORD_LEN];
    int idx;
    float score;
} WordCandidate;

static float compute_prophecy_debt_simple(float score) {
    /* Simplified: single candidate, debt is always 0 */
    (void)score;
    return 0.0f;
}

static WordCandidate select_word(const int *bpe_context, int bpe_ctx_len,
                                  const int *word_context, int n_wctx,
                                  int prev_word, int step_idx,
                                  const int *forbidden, int n_forbidden,
                                  int direction)
{
    float bpe_logits[BPE_VOCAB];
    forward_pass(bpe_context, bpe_ctx_len, bpe_logits);

    int n_words;
    float *logits;

    if (has_weights) {
        n_words = ext_vocab_n;
        logits = (float *)malloc(n_words * sizeof(float));
        bpe_logits_to_word_scores(bpe_logits, logits, 1);
        dario_overlay(logits, word_context, n_wctx, prev_word, step_idx, direction, 1);

        /* Mask forbidden by word string */
        for (int i = 0; i < n_words; i++) {
            for (int fi = 0; fi < n_forbidden; fi++) {
                int fidx = forbidden[fi];
                const char *fw;
                if (fidx < ext_vocab_n)
                    fw = ext_vocab[fidx].word;
                else if (fidx < NWORDS)
                    fw = WORDS[fidx];
                else
                    continue;
                if (strcmp(ext_vocab[i].word, fw) == 0) {
                    logits[i] = -1e9f;
                    break;
                }
            }
        }
    } else {
        n_words = NWORDS;
        logits = (float *)malloc(n_words * sizeof(float));
        bpe_logits_to_word_scores(bpe_logits, logits, 0);
        dario_overlay(logits, word_context, n_wctx, prev_word, step_idx, direction, 0);

        for (int fi = 0; fi < n_forbidden; fi++) {
            int f = forbidden[fi];
            if (f >= 0 && f < NWORDS) logits[f] = -1e9f;
        }
    }

    /* Build indexed array and sort (selection sort for top-k) */
    /* Find top TOP_K by repeated max scan */
    WordCandidate top[TOP_K];
    int top_n = 0;
    int *used = (int *)calloc(n_words, sizeof(int));

    for (int t = 0; t < TOP_K && t < n_words; t++) {
        int best = -1;
        float best_score = -1e30f;
        for (int i = 0; i < n_words; i++) {
            if (!used[i] && logits[i] > best_score) {
                best_score = logits[i];
                best = i;
            }
        }
        if (best < 0) break;
        used[best] = 1;

        if (has_weights) {
            strncpy(top[top_n].word, ext_vocab[best].word, MAX_WORD_LEN - 1);
            top[top_n].word[MAX_WORD_LEN - 1] = 0;
        } else {
            strncpy(top[top_n].word, WORDS[best], MAX_WORD_LEN - 1);
            top[top_n].word[MAX_WORD_LEN - 1] = 0;
        }
        top[top_n].idx = best;
        top[top_n].score = best_score;
        top_n++;
    }
    free(used);
    free(logits);

    if (top_n == 0) {
        /* Fallback */
        WordCandidate wc;
        strncpy(wc.word, "void", MAX_WORD_LEN);
        wc.idx = 0;
        wc.score = 0.0f;
        return wc;
    }

    /* Softmax sampling over top-k */
    float mx = top[0].score;
    float probs[TOP_K];
    float total = 0.0f;
    for (int i = 0; i < top_n; i++) {
        probs[i] = expf(top[i].score - mx);
        total += probs[i];
    }
    for (int i = 0; i < top_n; i++) probs[i] /= total;

    float r = rng_float();
    float cum = 0.0f;
    for (int i = 0; i < top_n; i++) {
        cum += probs[i];
        if (cum >= r) return top[i];
    }
    return top[0];
}

/* ===================================================================
 * ORIGIN EXTRACTION
 * =================================================================== */
static int extract_key(const char *text) {
    int ids[256];
    int n = tokenize_words(text, ids, 256);
    if (n == 0) return rng_int(NWORDS > 0 ? NWORDS : 1);

    int best = ids[0];
    float best_score = 0.0f;
    for (int i = 0; i < n; i++) {
        int wid = ids[i];
        float sc = 0.0f;
        for (int j = 0; j < n_cooc; j++) {
            if (cooc[j].a == wid || cooc[j].b == wid)
                sc += cooc[j].val;
        }
        if (sc > best_score) {
            best_score = sc;
            best = wid;
        }
    }
    return best;
}

/* ===================================================================
 * BI-DIRECTIONAL REASONING
 * =================================================================== */
typedef struct {
    char word[MAX_WORD_LEN];
    int idx;
    int step;
    int is_wormhole;
    float debt;
} StepInfo;

static void run_chain(const char *user_text,
                      char *seed_word_out, int *seed_idx_out,
                      StepInfo *fwd_steps, int *n_fwd,
                      StepInfo *back_steps, int *n_back)
{
    /* BPE encode input */
    int input_bpe[MAX_BPE_SEQ];
    int input_bpe_len = bpe_encode(user_text, input_bpe, MAX_BPE_SEQ);

    /* Word-level tokenize */
    int word_context[256];
    int n_wctx = tokenize_words(user_text, word_context, 256);

    int seed_idx = extract_key(user_text);
    const char *seed_word = (seed_idx < NWORDS) ? WORDS[seed_idx] : "void";
    strncpy(seed_word_out, seed_word, MAX_WORD_LEN - 1);
    seed_word_out[MAX_WORD_LEN - 1] = 0;
    *seed_idx_out = seed_idx;

    /* MetaJanus prophecy */
    float cal_d = calendar_dissonance(calendar_days_since_epoch());
    float predicted_entropy = 0.5f + 0.2f * prophecy_debt + 0.1f * cal_d + 0.15f * personal_dissonance();

    /* Step direction split */
    int n_backward = (int)(STEPS * (0.3f + 0.4f * prophecy_debt + 0.1f * cal_d));
    if (n_backward < 1) n_backward = 1;
    if (n_backward > STEPS - 1) n_backward = STEPS - 1;
    int n_forward = STEPS - n_backward;

    int forbidden[MAX_FORBIDDEN];
    int n_forbidden = 0;
    forbidden[n_forbidden++] = seed_idx;

    /* Two independent BPE contexts */
    int *fwd_bpe_ctx = (int *)malloc(MAX_BPE_SEQ * sizeof(int));
    int fwd_bpe_len = input_bpe_len;
    memcpy(fwd_bpe_ctx, input_bpe, input_bpe_len * sizeof(int));

    int *bwd_bpe_ctx = (int *)malloc(MAX_BPE_SEQ * sizeof(int));
    int bwd_bpe_len = input_bpe_len;
    memcpy(bwd_bpe_ctx, input_bpe, input_bpe_len * sizeof(int));

    /* Word contexts for each direction */
    int fwd_word_ctx[512], bwd_word_ctx[512];
    int n_fwd_wctx = n_wctx, n_bwd_wctx = n_wctx;
    memcpy(fwd_word_ctx, word_context, n_wctx * sizeof(int));
    memcpy(bwd_word_ctx, word_context, n_wctx * sizeof(int));

    int fwd_prev = seed_idx;
    int bwd_prev = seed_idx;

    *n_fwd = 0;
    *n_back = 0;

    float total_debt = 0.0f;
    int total_steps_done = 0;

    int max_steps = n_forward > n_backward ? n_forward : n_backward;

    for (int i = 0; i < max_steps; i++) {
        /* Generate BACKWARD word */
        if (i < n_backward) {
            update_chambers(n_forward + i);
            WordCandidate sel = select_word(bwd_bpe_ctx, bwd_bpe_len,
                                            bwd_word_ctx, n_bwd_wctx,
                                            bwd_prev, n_forward + i,
                                            forbidden, n_forbidden, -1);

            StepInfo *si = &back_steps[*n_back];
            strncpy(si->word, sel.word, MAX_WORD_LEN - 1);
            si->word[MAX_WORD_LEN - 1] = 0;
            si->idx = sel.idx;
            si->step = n_forward + i;
            si->is_wormhole = 0;
            si->debt = prophecy_debt;
            (*n_back)++;

            /* Extend backward BPE context */
            const int *wb; int wb_len;
            if (has_weights && sel.idx < ext_vocab_n) {
                wb = ext_vocab[sel.idx].bpe_ids;
                wb_len = ext_vocab[sel.idx].bpe_len;
            } else if (sel.idx < NWORDS) {
                wb = vocab_bpe[sel.idx];
                wb_len = vocab_bpe_len[sel.idx];
            } else {
                wb = NULL; wb_len = 0;
            }
            for (int j = 0; j < wb_len && bwd_bpe_len < MAX_BPE_SEQ; j++)
                bwd_bpe_ctx[bwd_bpe_len++] = wb[j];

            if (n_bwd_wctx < 512) bwd_word_ctx[n_bwd_wctx++] = sel.idx;

            if (n_forbidden < MAX_FORBIDDEN) forbidden[n_forbidden++] = sel.idx;
            prophecy_debt = 0.9f * prophecy_debt + 0.1f * compute_prophecy_debt_simple(sel.score);
            bwd_prev = sel.idx;
            total_debt += prophecy_debt;
            total_steps_done++;
        }

        /* Generate FORWARD word */
        if (i < n_forward) {
            update_chambers(i);
            int is_wormhole = 0;
            if (prophecy_debt < 0.2f && wormhole > 0.1f && rng_float() < wormhole)
                is_wormhole = 1;

            WordCandidate sel = select_word(fwd_bpe_ctx, fwd_bpe_len,
                                            fwd_word_ctx, n_fwd_wctx,
                                            fwd_prev, i,
                                            forbidden, n_forbidden, 1);

            StepInfo *si = &fwd_steps[*n_fwd];
            strncpy(si->word, sel.word, MAX_WORD_LEN - 1);
            si->word[MAX_WORD_LEN - 1] = 0;
            si->idx = sel.idx;
            si->step = i;
            si->is_wormhole = is_wormhole;
            si->debt = prophecy_debt;
            (*n_fwd)++;

            /* Extend forward BPE context */
            const int *wb; int wb_len;
            if (has_weights && sel.idx < ext_vocab_n) {
                wb = ext_vocab[sel.idx].bpe_ids;
                wb_len = ext_vocab[sel.idx].bpe_len;
            } else if (sel.idx < NWORDS) {
                wb = vocab_bpe[sel.idx];
                wb_len = vocab_bpe_len[sel.idx];
            } else {
                wb = NULL; wb_len = 0;
            }
            for (int j = 0; j < wb_len && fwd_bpe_len < MAX_BPE_SEQ; j++)
                fwd_bpe_ctx[fwd_bpe_len++] = wb[j];

            if (n_fwd_wctx < 512) fwd_word_ctx[n_fwd_wctx++] = sel.idx;

            /* Update cooc/bigrams */
            if (fwd_prev >= 0 && sel.idx < NWORDS && fwd_prev < NWORDS)
                bigram_update(fwd_prev, sel.idx);
            for (int ci = 0; ci < n_wctx; ci++) {
                if (sel.idx < NWORDS)
                    cooc_update(word_context[ci], sel.idx);
            }

            if (n_forbidden < MAX_FORBIDDEN) forbidden[n_forbidden++] = sel.idx;
            prophecy_debt = 0.9f * prophecy_debt + 0.1f * compute_prophecy_debt_simple(sel.score);
            fwd_prev = sel.idx;
            total_debt += prophecy_debt;
            total_steps_done++;
        }
    }

    /* Evaluate MetaJanus prophecy */
    float avg_debt = total_steps_done > 0 ? total_debt / total_steps_done : 0.0f;
    float error = fabsf(predicted_entropy - avg_debt);
    META.prophecy_accuracy = 0.9f * META.prophecy_accuracy + 0.1f * (1.0f - error);
    META.total_predictions++;

    free(fwd_bpe_ctx);
    free(bwd_bpe_ctx);
}

