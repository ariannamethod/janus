static float chambers[CH_N] = {0};
static const float CH_DECAY[CH_N] = {0.95f, 0.95f, 0.93f, 0.96f, 0.94f, 0.97f};

static void update_chambers(int step_idx) {
    float depth = (float)step_idx / STEPS;
    if (depth < 0.33f) chambers[CH_FLOW] += 0.05f;
    else if (depth < 0.66f) chambers[CH_FEAR] += 0.04f;
    else chambers[CH_VOID] += 0.05f;
    if (depth > 0.75f) chambers[CH_COMPLEX] += 0.03f;
    if (trauma > 0.3f) chambers[CH_RAGE] += 0.04f;

    float K = 0.02f;
    float old[CH_N];
    for (int i = 0; i < CH_N; i++) old[i] = chambers[i];
    for (int i = 0; i < CH_N; i++) {
        for (int j = 0; j < CH_N; j++) {
            if (i != j) chambers[i] += K * sinf(old[j] - old[i]);
        }
        chambers[i] = chambers[i] * CH_DECAY[i];
        if (chambers[i] < 0.0f) chambers[i] = 0.0f;
        if (chambers[i] > 1.0f) chambers[i] = 1.0f;
    }
}

/* ===================================================================
 * CO-OCCURRENCE + BIGRAMS (hash table approach)
 * =================================================================== */
typedef struct { int a, b; float val; } CoocEntry;
typedef struct { int prev, next; float val; } BigramEntry;

static CoocEntry cooc[MAX_COOC];
static int n_cooc = 0;
static BigramEntry bigrams_arr[MAX_BIGRAM];
static int n_bigrams = 0;

static int cooc_find(int w1, int w2) {
    int a = w1 < w2 ? w1 : w2;
    int b = w1 < w2 ? w2 : w1;
    for (int i = 0; i < n_cooc; i++) {
        if (cooc[i].a == a && cooc[i].b == b) return i;
    }
    return -1;
}

static void cooc_update(int w1, int w2) {
    int a = w1 < w2 ? w1 : w2;
    int b = w1 < w2 ? w2 : w1;
    int idx = cooc_find(w1, w2);
    if (idx >= 0) {
        cooc[idx].val += 1.0f;
    } else if (n_cooc < MAX_COOC) {
        cooc[n_cooc].a = a;
        cooc[n_cooc].b = b;
        cooc[n_cooc].val = 1.0f;
        n_cooc++;
    }
}

static float cooc_get(int w1, int w2) {
    int idx = cooc_find(w1, w2);
    return idx >= 0 ? cooc[idx].val : 0.0f;
}

static int bigram_find(int prev, int nxt) {
    for (int i = 0; i < n_bigrams; i++) {
        if (bigrams_arr[i].prev == prev && bigrams_arr[i].next == nxt) return i;
    }
    return -1;
}

static void bigram_update(int prev, int nxt) {
    int idx = bigram_find(prev, nxt);
    if (idx >= 0) {
        bigrams_arr[idx].val += 1.0f;
    } else if (n_bigrams < MAX_BIGRAM) {
        bigrams_arr[n_bigrams].prev = prev;
        bigrams_arr[n_bigrams].next = nxt;
        bigrams_arr[n_bigrams].val = 1.0f;
        n_bigrams++;
    }
}

static float bigram_get(int prev, int nxt) {
    int idx = bigram_find(prev, nxt);
    return idx >= 0 ? bigrams_arr[idx].val : 0.0f;
}

